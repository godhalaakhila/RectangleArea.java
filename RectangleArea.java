public class RectangleArea {
    public static void main(String[] args) {
        // Using int data type
        int lengthInt = 10;
        int widthInt = 5;
        int areaInt = lengthInt * widthInt;
        System.out.println("Area of rectangle (using int): " + areaInt);

        // Using float data type
        float lengthFloat = 10.5f;
        float widthFloat = 5.2f;
        float areaFloat = lengthFloat * widthFloat;
        System.out.println("Area of rectangle (using float): " + areaFloat);
    }
}
 
    

